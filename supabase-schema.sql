-- ==== BUCKET DE ARMAZENAMENTO ====
-- Criação do bucket para os documentos caso não exista
INSERT INTO storage.buckets (id, name, public) VALUES ('documentos', 'documentos', true) ON CONFLICT DO NOTHING;

-- Políticas de segurança para o bucket 'documentos'
-- Leitura: Permite que qualquer pessoa veja os arquivos (necessário para getPublicUrl se o bucket for público)
CREATE POLICY "Leitura pública de documentos" ON storage.objects FOR SELECT USING (bucket_id = 'documentos');

-- Inserção: Restrito a usuários autenticados para evitar abusos no armazenamento
CREATE POLICY "Upload restrito a usuários autenticados" ON storage.objects FOR INSERT TO authenticated WITH CHECK (bucket_id = 'documentos');

-- Deleção: Restrito a usuários autenticados
CREATE POLICY "Deleção restrita a usuários autenticados" ON storage.objects FOR DELETE TO authenticated USING (bucket_id = 'documentos');
